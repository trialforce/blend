<?php

namespace Validator;

/**
 * Validator Ip V4 & V6
 */
class Ip extends \Validator\Validator
{

    /**
     * Validate CEP
     * @param string $value
     * @return array
     */
    public function validate($value = NULL)
    {
        $error = parent::validate($value);

        if (mb_strlen($this->value) > 0 && !self::validateIp($this->value))
        {
            $error[] = 'IP invÃ¡lido.';
        }

        return $error;
    }

    /**
     * Avoid hacking attempt, when havving commands in plac of ip
     * Validate ipv4 e ipv6
     *
     * @param string|null $ip o ip
     * @return bool
     */
    public static function validateIp(string|null $ip)
    {
        $result =  preg_match("/((^\s*((([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5]))\s*$)|(^\s*((([0-9A-Fa-f]{1,4}:){7}([0-9A-Fa-f]{1,4}|:))|(([0-9A-Fa-f]{1,4}:){6}(:[0-9A-Fa-f]{1,4}|((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){5}(((:[0-9A-Fa-f]{1,4}){1,2})|:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3})|:))|(([0-9A-Fa-f]{1,4}:){4}(((:[0-9A-Fa-f]{1,4}){1,3})|((:[0-9A-Fa-f]{1,4})?:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){3}(((:[0-9A-Fa-f]{1,4}){1,4})|((:[0-9A-Fa-f]{1,4}){0,2}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){2}(((:[0-9A-Fa-f]{1,4}){1,5})|((:[0-9A-Fa-f]{1,4}){0,3}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(([0-9A-Fa-f]{1,4}:){1}(((:[0-9A-Fa-f]{1,4}){1,6})|((:[0-9A-Fa-f]{1,4}){0,4}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:))|(:(((:[0-9A-Fa-f]{1,4}){1,7})|((:[0-9A-Fa-f]{1,4}){0,5}:((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)(\.(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)){3}))|:)))(%.+)?\s*$))/", $ip);
        return (bool)$result;
    }

    /**
     * Tweak header values of IP, in case of multiple ip or other weird things
     * @param string $ip
     * @return string
     */
    public static function parse(string $ip)
    {
        //in the case of comma, when are two ips, get only the first
        if (stripos($ip, ',') > 0)
        {
            $explode = explode(',', $ip);
            $ip = trim($explode[0]);
        }

        //removes comma, make trim and some weird cases
        $ip = trim(str_replace(',', '', $ip));

        return $ip;
    }

}
